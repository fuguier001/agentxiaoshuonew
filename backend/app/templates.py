def get_template(category: str, template_id: str) -> Optional[Dict[str, Any]]:
    """获取模板"""
    if category == "outline":
        return ProfessionalTemplates.get_outline_template(template_id)
    elif category == "character":
        return ProfessionalTemplates.get_character_template(template_id)
    elif category == "chapter":
        return ProfessionalTemplates.get_chapter_template(template_id)
    elif category == "plot":
        return ProfessionalTemplates.get_plot_template(template_id)
    return None


def list_templates() -> Dict[str, List[Dict[str, Any]]]:
    """列出所有模板"""
    return ProfessionalTemplates.list_all_templates()
