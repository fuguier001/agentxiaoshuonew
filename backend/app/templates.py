    def get_outline_template(cls, template_id: str) -> Optional[Dict[str, Any]]:
        """获取大纲模板"""
        return cls.OUTLINE_TEMPLATES.get(template_id)
    
    @classmethod
    def get_character_template(cls, template_id: str) -> Optional[Dict[str, Any]]:
        """获取人物模板"""
        return cls.CHARACTER_TEMPLATES.get(template_id)
    
    @classmethod
    def get_chapter_template(cls, template_id: str) -> Optional[Dict[str, Any]]:
        """获取章节模板"""
        return cls.CHAPTER_TEMPLATES.get(template_id)
    
    @classmethod
    def get_plot_template(cls, template_id: str) -> Optional[Dict[str, Any]]:
        """获取情节模板"""
        return cls.PLOT_TEMPLATES.get(template_id)
    
    @classmethod
    def list_all_templates(cls) -> Dict[str, List[Dict[str, Any]]]:
        """列出所有模板"""
        return {
            "outline": list(cls.OUTLINE_TEMPLATES.values()),
            "character": list(cls.CHARACTER_TEMPLATES.values()),
            "chapter": list(cls.CHAPTER_TEMPLATES.values()),
            "plot": list(cls.PLOT_TEMPLATES.values())
        }


